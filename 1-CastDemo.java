//This demo is for Implicit Conversion and Explicit Conversion.

class CastDemo
{
	     public static void main(String[] args)
	     {
   		float var1 = 10;  //implicit conversion
		int var2 = (int)10.7;   //Explicit Conversion

		System.out.println("The value of var1 is"+var1);
		System.out.println("The value of var2 is"+var2);
     	     }
}

